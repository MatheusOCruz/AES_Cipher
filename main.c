void shift_rows_dec(u8 aes_matrix[4][4]){
    u8 temp_row[4];
    for (int i = 1; i < 4; ++i) {
        for (int j = 0; j < 4; ++j) {
            temp_row[(j + i) % 4] = aes_matrix[i][j];
        }
        for (int j = 0; j < 4; ++j) {
            aes_matrix[i][j] = temp_row[j];
        }
    }
}

void sub_bytes_dec(u8 *aes_array){
    for (int i = 0; i < 16; ++i) {
        aes_array[i] = reverse_s_box[aes_array[i]];
    }
}

void mix_columns_dec(u8 aes_matrix[4][4]){
    u8 temp_matrix[4][4];
    for (int i = 0; i < 4; ++i) {
        for (int j = 0; j < 4; ++j) {
            u8 result = 0;
            for (int k = 0; k < 4; ++k) {
                u8 val = aes_matrix[k][j];
                u8 fac = mix_dec[i][k];
                switch (fac) {
                    case 9:
                        result ^= mul9[val];
                        break;
                    case 11:
                        result ^= mul11[val];
                        break;
                    case 13:
                        result ^= mul13[val];
                        break;
                    case 14:
                        result ^= mul14[val];
                        break;
                    default:
                        break;
                }
            }
            temp_matrix[i][j] = result;
        }
    }
    for (int i = 0; i < 4; ++i) {
        for (int j = 0; j < 4; ++j) {
            aes_matrix[i][j] = temp_matrix[i][j];
        }
    }
}

void AES_encrypt(char *plaintext, u8 *expanded_key, int rounds){
    u8 aes_matrix[4][4];
    u8 aes_array[16];
    for (int i = 0; i < 16; ++i) aes_array[i] = plaintext[i];
    add_round_key(aes_array, expanded_key);
    for (int i = 0; i < rounds - 1; ++i) {
        sub_bytes(aes_array);
        for (int j = 0; j < 4; ++j) {
            for (int k = 0; k < 4; ++k) {
                aes_matrix[k][j] = aes_array[(j * 4) + k];
            }
        }
        shift_rows(aes_matrix);
        mix_columns(aes_matrix);
        for (int j = 0; j < 4; ++j) {
            for (int k = 0; k < 4; ++k) {
                aes_array[(j * 4) + k] = aes_matrix[k][j];
            }
        }
        add_round_key(aes_array, expanded_key + (16 * (i + 1)));
    }
    sub_bytes(aes_array);
    for (int j = 0; j < 4; ++j) {
        for (int k = 0; k < 4; ++k) {
            aes_matrix[k][j] = aes_array[(j * 4) + k];
        }
    } // array to matrix
    shift_rows(aes_matrix);
    for (int j = 0; j < 4; ++j) {
        for (int k = 0; k < 4; ++k) {
            aes_array[(j * 4) + k] = aes_matrix[k][j];
        }
    } // matrix to array
    add_round_key(aes_array, expanded_key + 16*rounds);
    for (int i = 0; i < 16; ++i)  plaintext[i] = (char)aes_array[i];
}

void AES_decrypt(char *ciphertext, u8 *expanded_key, int rounds){
    u8 aes_matrix[4][4];
    u8 aes_array[16];
    for (int i = 0; i < 16; ++i) aes_array[i] = ciphertext[i];
    add_round_key(aes_array, expanded_key + 16*rounds);
    for (int j = 0; j < 4; ++j) {
        for (int k = 0; k < 4; ++k) {
            aes_matrix[k][j] = aes_array[(j * 4) + k];
        }
    } // array to matrix

    for (int i = (rounds -1) ; i > 0; i--) {
        shift_rows_dec(aes_matrix);
        for (int j = 0; j < 4; ++j) {
            for (int k = 0; k < 4; ++k) {
                aes_array[(j * 4) + k] = aes_matrix[k][j];
            }
        } // matrix to array
        sub_bytes_dec(aes_array);
        add_round_key(aes_array, expanded_key +  (16 * i));
        for (int j = 0; j < 4; ++j) {
            for (int k = 0; k < 4; ++k) {
                aes_matrix[k][j] = aes_array[(j * 4) + k];
            }
        } // array to matrix
        mix_columns_dec(aes_matrix);
    }
    shift_rows_dec(aes_matrix);
    for (int j = 0; j < 4; ++j) {
        for (int k = 0; k < 4; ++k) {
            aes_array[(j * 4) + k] = aes_matrix[k][j];
        }
    } // matrix to array
    sub_bytes_dec(aes_array);
    add_round_key(aes_array, expanded_key);
    for (int i = 0; i < 16; ++i) ciphertext[i] = aes_array[i];
}


void CTR_AES_encrypt(char *plaintext, u8 *expanded_key, int rounds, u8* init_vector, int counter){
    u8 full_vec[16];
    for (int i = 0; i < 12; ++i) full_vec[i] = init_vector[i];
    for (int i = 15; i > 11; i--) {
        full_vec[i] = (u8)(counter & 0xFF);
        counter >>= 8;
    }
    AES_encrypt(full_vec, expanded_key, rounds);
    for (int i = 0; i < 16; ++i) {
        plaintext[i] ^=full_vec[i];
    }
}

void CTR_AES_decrypt(char *ciphertext, u8 *expanded_key, int rounds, u8* init_vector, int counter){
    u8 full_vec[16];
    for (int i = 0; i < 12; ++i) {
        full_vec[i] = init_vector[i];
    }
    for (int i = 15; i > 11; i--) {
        full_vec[i] = (u8)(counter & 0xFF);
        counter >>= 8;
    }
    AES_encrypt(full_vec, expanded_key, rounds);
    for (int i = 0; i < 16; ++i) {
        ciphertext[i] ^=full_vec[i];
    }
}


void GF_128(const u8* x, const u8* Y, u8* result){
    u128 Z, V = 0,y = 0,mask, r;
    Z = 0;

    for (int i = 0; i < 16; i++) {
        V |= (u128) x[i] << (8 * (15 - i));
    }

    for (int i = 0; i < 16; i++) {
        y |= (u128) Y[i] << (8 * (15 - i));
    }

    r = 0;
    r |= (u128)0xe1<<120;

    for (int i = 127; i >=0 ; --i) {
        mask = ((u128)1)<<i;
        u8 bit_y = (y & mask)? 1 : 0;
        if (bit_y == 1) Z ^= V;

        if(V & 0x01) V = (V>>1) ^ r;
        else V >>= 1;
    }
    for (int i = 0; i < 16; i++) {
        result[i] = (u8)(Z >> (8 * (15 - i)));
    }
}

